#define F_CPU 8000000UL

#include <avr/io.h>
#include <util/delay.h>

#define BIT(x) (1 << (x))

int main(void) {
	DDRD = 0b11111111; // Make pins D0 t/m D7 output
	
	// create a lookup table with some led patterns
	int lookupTable[] = {
		0b00000000, 0b00000001, 0b00000011, 0b00000111, 0b00001111, 0b00011111, 0b00111111, 0b01111111, 0b11111111,
		0b11111110, 0b11111100, 0b11111000, 0b11110000, 0b11100000, 0b11000000, 0b10000000, 0b00000000,
		0b00000000, 0b00000001, 0b00000011, 0b00000111, 0b00001111, 0b00011111, 0b00111111, 0b01111111, 0b11111111,
		0b11111110, 0b11111100, 0b11111000, 0b11110000, 0b11100000, 0b11000000, 0b10000000, 0b00000000,
		
		0b00000001, 0b00000011, 0b00000110, 0b00001100, 0b00011000, 0b00110000, 0b01100000, 0b11000000, 0b10000000, 0b00000000,
		0b00000001, 0b00000011, 0b00000110, 0b00001100, 0b00011000, 0b00110000, 0b01100000, 0b11000000, 0b10000000, 0b00000000,
		
		0b10000001, 0b11000011, 0b01100110, 0b00111100, 0b00000000,
		0b10000001, 0b11000011, 0b01100110, 0b00111100, 0b00000000,
		
		0b10000001, 0b01000010, 0b00100100, 0b00011000, 0b00000000,
		0b10000001, 0b01000010, 0b00100100, 0b00011000, 0b00000000,
		
		0b10100101, 0b01011010, 
		0b10100101, 0b01011010, 0b00000000,
	};
	
	while (1) {
		// Go over each value in the lookup table
		for (int i = 0; i < 79; i++) {
			PORTD = lookupTable[i];
			_delay_ms(100);
		}
	}
}