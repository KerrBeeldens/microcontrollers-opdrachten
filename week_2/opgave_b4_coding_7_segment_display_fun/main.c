#define F_CPU 8000000UL

#include <avr/io.h>
#include <util/delay.h>
#include <stdbool.h>

#define BIT(x) (1 << (x))

int main(void) {
	DDRB = 0b11111111; // Make pins B0 t/m B7 output
	
	// create a lookup table with some seven segment display patterns
	const unsigned char lookupTable [84] = {
		0b00000000, 0b00000001, 0b00000011, 0b00000111, 0b00001111, 0b00011111, 0b00111111,
		0b00000000, 0b00000001, 0b00000011, 0b00000111, 0b00001111, 0b00011111, 0b00111111,
		
		0b00000000, 0b00100000, 0b00110000, 0b00111000, 0b00111100, 0b00111110,
		0b00111111, 0b00011111, 0b00001111, 0b00000111, 0b00000011, 0b00000001, 0b00000000,
		0b00000000, 0b00100000, 0b00110000, 0b00111000, 0b00111100, 0b00111110,
		0b00111111, 0b00011111, 0b00001111, 0b00000111, 0b00000011, 0b00000001, 0b00000000,
		
		0b00000110, 0b01001111, 0b01111111, 0b01111001, 0b00110000, 0b00000000,
		0b00110000, 0b01111001, 0b01111111, 0b01001111, 0b00000110, 0b00000000,
		0b00000110, 0b01001111, 0b01111111, 0b01111001, 0b00110000, 0b00000000,
		0b00110000, 0b01111001, 0b01111111, 0b01001111, 0b00000110, 0b00000000,
		
		0b00000001, 0b00100011, 0b01100011, 0b01110111, 0b01111111,
		0b01111110, 0b01011100, 0b00011100, 0b00001000, 0b00000000,
		0b00000001, 0b00100011, 0b01100011, 0b01110111, 0b01111111,
		0b01111110, 0b01011100, 0b00011100, 0b00001000, 0b00000000,
	};


	while (1) {
		// Go over each value in the lookup table
		for (int i = 0; i < 84; i++) {
			PORTB = lookupTable[i];
			_delay_ms(100);
		}
	}
}